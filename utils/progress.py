import sys


#------------------------------------------------------------------------------
#    Progress print
#------------------------------------------------------------------------------
def print_progress(i, N, prefix="", suffix=""):
    percent = (i+1) * 100 / N
    k = int(percent / 5)
    str_progress = "\r{} [{}{}] {:.2f}% {}".format(
    				prefix, "="*k, " "*(20-k), percent, suffix)
    sys.stdout.flush()
    sys.stdout.write(str_progress)